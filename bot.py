    question_index = context.user_data['current_question']
    question_data = WORDS_DB[question_index]
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
    correct_answer = ["A", "B", "C"][question_data['correct_index']]
    
