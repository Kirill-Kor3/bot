    if user_answer == correct_answer:
        context.user_data['score'] += 1
        result_text = "‚úÖ **–ü—Ä–∞–≤–∏–ª—å–Ω–æ!** –û—Ç–ª–∏—á–Ω–∞—è —Ä–∞–±–æ—Ç–∞! üéâ"
